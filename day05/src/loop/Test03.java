package loop;

public class Test03 {

	public static void main(String[] args) {
		//반복수의 활용 
		
		//ex, 1부터 100까지 출력 
		for(int i=1 ; i <=100 ; i++) {
			System.out.println(i);
		}
		
		//ex, 100부터 1까지 출력 
		//for(int i=100; i>0 ; i=i-1) 가능 
		for(int i=100 ; i > 0 ; i--) {
			System.out.println(i);
		}
		

	}

}
